import fs from 'node:fs';
import path from 'node:path';
import {fileURLToPath} from 'node:url';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);
const workspaceRoot = path.resolve(__dirname, '..');
const referenceDir = path.join(workspaceRoot, 'public', 'reference');
const readinessPath = path.join(referenceDir, 'readiness-output.txt');
const burnDebugPath = path.join(referenceDir, 'burn-debug-output.json');
const outputPath = path.join(workspaceRoot, 'src', 'lib', 'generatedReferenceData.ts');

function readText(filePath) {
  if (!fs.existsSync(filePath)) {
    throw new Error(`Missing required reference file: ${filePath}`);
  }
  return fs.readFileSync(filePath, 'utf8');
}

function readJson(filePath) {
  return JSON.parse(readText(filePath));
}

function parseReadinessReport(text) {
  const lines = text.split(/\r?\n/);
  const readinessHeaderIndex = lines.indexOf('[demo-readiness]');
  const confirmedHeaderIndex = lines.indexOf('[confirmed-avoids]');

  if (readinessHeaderIndex === -1) {
    throw new Error('Expected [demo-readiness] section in readiness-output.txt');
  }

  const reportLines = lines.slice(readinessHeaderIndex + 1, confirmedHeaderIndex === -1 ? lines.length : confirmedHeaderIndex).filter(Boolean);
  const confirmedLines = confirmedHeaderIndex === -1 ? [] : lines.slice(confirmedHeaderIndex + 1).filter(Boolean);

  return {
    report: JSON.parse(reportLines.join('\n')),
    confirmedAvoids: confirmedLines.map(line => JSON.parse(line)),
  };
}

function formatDistance(km) {
  if (!Number.isFinite(km)) return '--';
  if (Math.abs(km) < 1) {
    return `${(km * 1000).toFixed(1)} m`;
  }
  return `${km.toFixed(3)} km`;
}

function formatDeltaV(kmPerSecond) {
  if (!Number.isFinite(kmPerSecond)) return '--';
  return `${(kmPerSecond * 1000).toFixed(2)} m/s`;
}

function formatFuelKg(kg) {
  if (!Number.isFinite(kg)) return '--';
  return kg < 1 ? `${kg.toFixed(4)} kg` : `${kg.toFixed(2)} kg`;
}

function formatLeadTimeMinutes(burn) {
  const burnEpochMs = Date.parse(burn?.burn_epoch ?? '');
  const tcaEpochMs = Number.isFinite(burn?.trigger_tca_epoch_s)
    ? burn.trigger_tca_epoch_s * 1000
    : Date.parse(burn?.trigger_tca ?? '');

  if (!Number.isFinite(burnEpochMs) || !Number.isFinite(tcaEpochMs)) return '--';

  const deltaMinutes = (tcaEpochMs - burnEpochMs) / 60000;
  if (!Number.isFinite(deltaMinutes)) return '--';
  return `~${deltaMinutes.toFixed(1)} min`;
}

function buildBurnDecision(burn) {
  if (!burn) {
    return {
      id: 'BURN-UNAVAILABLE',
      satellite: 'SAT-UNKNOWN',
      debris: 'DEB-UNKNOWN',
      missBefore: '--',
      missAfter: '--',
      deltaV: '--',
      uploadStation: '--',
      leadTime: '--',
    };
  }

  return {
    id: burn.id ?? 'BURN-UNAVAILABLE',
    satellite: burn.satellite_id ?? 'SAT-UNKNOWN',
    debris: burn.trigger_debris_id ?? 'DEB-UNKNOWN',
    missBefore: formatDistance(burn.trigger_miss_distance_km),
    missAfter: formatDistance(burn.mitigation_miss_distance_km),
    deltaV: formatDeltaV(burn.delta_v_norm_km_s),
    uploadStation: burn.upload_station ?? '--',
    leadTime: formatLeadTimeMinutes(burn),
  };
}

function sortByBurnEpoch(a, b) {
  return Date.parse(a?.burn_epoch ?? '') - Date.parse(b?.burn_epoch ?? '');
}

function toTsLiteral(value) {
  return JSON.stringify(value, null, 2);
}

function writeGeneratedModule({readinessLines, burnDecision, burnDecisionSecondary, verificationStats}) {
  const output = `// Generated by scripts/sync-reference-data.mjs. Do not edit by hand.\n\nexport interface GeneratedBurnDecision {\n  id: string;\n  satellite: string;\n  debris: string;\n  missBefore: string;\n  missAfter: string;\n  deltaV: string;\n  uploadStation: string;\n  leadTime: string;\n}\n\nexport interface GeneratedVerificationStat {\n  label: string;\n  value: string;\n  accent: 'accent' | 'primary' | 'warning';\n}\n\nexport const readinessLines: readonly string[] = ${toTsLiteral(readinessLines)};\n\nexport const burnDecision: GeneratedBurnDecision = ${toTsLiteral(burnDecision)};\n\nexport const burnDecisionSecondary: GeneratedBurnDecision | null = ${burnDecisionSecondary ? toTsLiteral(burnDecisionSecondary) : 'null'};\n\nexport const verificationStats: readonly GeneratedVerificationStat[] = ${toTsLiteral(verificationStats)};\n`;

  fs.writeFileSync(outputPath, output, 'utf8');
}

function main() {
  const readiness = parseReadinessReport(readText(readinessPath));
  const burnDebug = readJson(burnDebugPath);
  const trackedBurns = Array.isArray(burnDebug.executed)
    ? burnDebug.executed
        .filter(burn => burn?.scheduled_from_predictive_cdm && burn?.trigger_debris_id)
        .sort(sortByBurnEpoch)
    : [];

  const report = readiness.report ?? {};
  const primaryBurn = trackedBurns[0] ?? null;
  const secondaryBurn = trackedBurns[1] ?? null;
  const readinessLines = [
    '[demo-readiness]',
    '{',
    `  "verdict": ${JSON.stringify(report.verdict ?? 'unknown')},`,
    `  "successful_avoids": ${JSON.stringify(report.successful_avoids ?? 0)},`,
    `  "fleet_collisions_avoided": ${JSON.stringify(report.fleet_collisions_avoided ?? 0)},`,
    `  "executed_burns": ${JSON.stringify(report.executed_burns ?? 0)},`,
    `  "dropped_burns": ${JSON.stringify(report.dropped_burns ?? 0)},`,
    `  "fuel_consumed_kg": ${JSON.stringify(report.fuel_consumed_kg ?? 0)}`,
    '}',
    '[confirmed-avoids]',
    ...readiness.confirmedAvoids.slice(0, 3).map(row => `${row.satellite_id ?? 'SAT-UNKNOWN'} -> ${row.trigger_debris_id ?? 'DEB-UNKNOWN'}`),
  ];

  const verificationStats = [
    {label: 'Successful avoids', value: String(report.successful_avoids ?? 0), accent: 'accent'},
    {label: 'Dropped burns', value: String(report.dropped_burns ?? 0), accent: 'primary'},
    {label: 'Fuel used', value: formatFuelKg(report.fuel_consumed_kg ?? 0), accent: 'warning'},
  ];

  writeGeneratedModule({
    readinessLines,
    burnDecision: buildBurnDecision(primaryBurn),
    burnDecisionSecondary: secondaryBurn ? buildBurnDecision(secondaryBurn) : null,
    verificationStats,
  });

  console.log(`[sync] wrote ${path.relative(workspaceRoot, outputPath)}`);
}

main();
